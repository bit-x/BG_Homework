vse_gls = ["а", "е", "ё", "и", "о", "у", "ы", "э", "ю", "я"]
vse_sgls = ["б", "в", "г", "д", "ж", "з", "й", "к", "л", "м", "н", "п", "р", "с", "т", "ф", "х", "ц", "ч", "щ", "ь", "ъ"]
poem = 'Муха села на варенье, Вот и всё стихотворенье!'
g = 0
s = 0
for i in poem.lower():
    if i in vse_gls:
        g += 1
    elif i in vse_sgls:
        s += 1
print(g, s)
